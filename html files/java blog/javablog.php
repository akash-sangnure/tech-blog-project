            <h1 style="text-align: center; color:blue; text-decoration:underline; text-shadow:3px 1px yellow; ">JAVA Programming</h1>
            
            <br><strong>Java</strong> is a high-level programming language originally developed by Sun Microsystems and released in 1995. Java runs on a variety of platforms, such as Windows, Mac OS, and the various versions of UNIX. This tutorial gives a complete understanding of Java. This reference will take you through simple and practical approaches while learning Java Programming language.
            
            <div style="text-align: center;"><br><strong><h3>Why to Learn java Programming?</h3></strong><br></div>
            Java is a MUST for students and working professionals to become a great Software Engineer specially when they are working in Software Development Domain. I will list down some of the key advantages of learning Java Programming:
            <ul>
                <li><strong>Object Oriented</strong> − In Java, everything is an Object. Java can be easily extended since it is based on the Object model.</li>
                <li><strong>Platform Independent</strong> − Unlike many other programming languages including C and C++, when Java is compiled, it is not compiled into platform specific machine, rather into platform independent byte code. This byte code is distributed over the web and interpreted by the Virtual Machine (JVM) on whichever platform it is being run on.</li>
                <li><strong>Simple</strong>  − Java is designed to be easy to learn. If you understand the basic concept of OOP Java, it would be easy to master.</li>
                <li><strong>Secure</strong>  − With Java's secure feature it enables to develop virus-free, tamper-free systems. Authentication techniques are based on public-key encryption.</li>
                <li><strong>Architecture-neutral</strong>  − Java compiler generates an architecture-neutral object file format, which makes the compiled code executable on many processors, with the presence of Java runtime system.</li>
                <li><strong>Portable</strong>  − Being architecture-neutral and having no implementation dependent aspects of the specification makes Java portable. Compiler in Java is written in ANSI C with a clean portability boundary, which is a POSIX subset.</li>
                <li><strong>Robust</strong>  − Java makes an effort to eliminate error prone situations by emphasizing mainly on compile time error checking and runtime checking.</li>
            </ul>

            <div style="text-align: center;"><br><strong><h3>Applications of Java Programming:</h3></strong><br></div>
            The latest release of the Java Standard Edition is Java SE 8. With the advancement of Java and its widespread popularity, multiple configurations were built to suit various types of platforms. For example: J2EE for Enterprise Applications, J2ME for Mobile Applications.
            The new J2 versions were renamed as Java SE, Java EE, and Java ME respectively. Java is guaranteed to be Write Once, Run Anywhere.
            <ul>
                <li><strong>Multithreaded</strong> − With Java's multithreaded feature it is possible to write programs that can perform many tasks simultaneously. This design feature allows the developers to construct interactive applications that can run smoothly.</li>
                <li><strong>Interpreted</strong> − Java byte code is translated on the fly to native machine instructions and is not stored anywhere. The development process is more rapid and analytical since the linking is an incremental and light-weight process.</li>
                <li><strong>High Performance</strong> − With the use of Just-In-Time compilers, Java enables high performance.</li>
                <li><strong>Distributed</strong> − Java is designed for the distributed environment of the internet.</li>
                <li><strong>Dynamic</strong> − Java is considered to be more dynamic than C or C++ since it is designed to adapt to an evolving environment. Java programs can carry extensive amount of run-time information that can be used to verify and resolve accesses to objects on run-time.</li>
            </ul>
            
